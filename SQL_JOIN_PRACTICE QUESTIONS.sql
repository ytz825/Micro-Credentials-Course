-- 10. Retrieve the employee first name, last name, dependent first name, last name, birth date for dependents who have birth dates less than or equal to 11/02/2008. 
SELECT E.FIRSTNAME EMPLOYEE_FIRSTNAME,E.LASTNAME EMPLOYEE_LASTNAME,D.FIRSTNAME DEPENDENT_FIRSTNAME,D.LASTNAME DEPENDENT_LASTNAME, D.BIRTHDATE DEPENDENT_BIRTHDATE
