-- 7. Retrieve the employee first name, last name, department name and group name for the employee named Gail Erickson.
SELECT TRIM(E.FIRSTNAME),E.LASTNAME,D.DEPARTMENTNAME,D.GROUPNAME
FROM EMPLOYEES E
INNER JOIN EMPLOYEESDEPARTMENTS ED
ON E.EMPLOYEEID = ED.EMPLOYEEID
INNER JOIN DEPARTMENTS D
ON ED.DEPARTMENTID = D.DEPARTMENTID
WHERE E.FIRSTNAME = 'Gail'
AND E.LASTNAME = 'Erickson'

-- 8. Retrieve the employee first name, last name, address, city, state, zip, department name and group name 
-- for the employee named David Bradley.
