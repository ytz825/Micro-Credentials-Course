-- 1. Create a list of employee names and their full addresses using the INNER JOIN.
SELECT E.EMPLOYEEID,E.FIRSTNAME,E.MIDDLENAME,E.LASTNAME,A.ADDRESSLINE1,A.ADDRESSLINE2,A.CITY,A.STATE,A.ZIP1,A.ZIP2
FROM EMPLOYEES E
INNER JOIN ADDRESSES A
ON E.EMPLOYEEID = A.EMPLOYEEID

-- 2. Create a list of employee names and their full addresses using the INNER JOIN for employees who live 
-- either in Massachusetts or California.
SELECT E.EMPLOYEEID,E.FIRSTNAME,E.MIDDLENAME,E.LASTNAME,A.ADDRESSLINE1,A.ADDRESSLINE2,A.CITY,A.STATE,A.ZIP1,A.ZIP2
FROM EMPLOYEES E
INNER JOIN ADDRESSES A
ON E.EMPLOYEEID = A.EMPLOYEEID
WHERE STATE IN ('MA','CA')

-- 3. Create an example of a Cartesian product using the Employees and Addresses table.
SELECT E.EMPLOYEEID,E.FIRSTNAME,E.MIDDLENAME,E.LASTNAME,A.ADDRESSLINE1,A.ADDRESSLINE2,A.CITY,A.STATE,A.ZIP1,A.ZIP2
FROM EMPLOYEES E
CROSS JOIN ADDRESSES A

-- 4. Use a LEFT OUTER JOIN to list all employee first and last names as well as the gender and birthdates 
-- of their dependents if they have any.
SELECT E.EMPLOYEEID,E.FIRSTNAME,E.LASTNAME,D.FIRSTNAME CHILDREN_NAME,D.GENDER CHILDREN_GENDER, D.BIRTHDATE CHILDREN_BIRTHDATE
FROM EMPLOYEES E
LEFT OUTER JOIN DEPENDENTS D
ON E.EMPLOYEEID = D.EMPLOYEEID

-- 5. Use a RIGHT OUTER JOIN to list all employee first and last names as well as the gender and birthdates of their dependents 
-- if they have any (Same as above but change location of tables).
SELECT E.EMPLOYEEID,E.FIRSTNAME,E.LASTNAME,D.FIRSTNAME CHILDREN_NAME,D.GENDER CHILDREN_GENDER, D.BIRTHDATE CHILDREN_BIRTHDATE
FROM DEPENDENTS D
RIGHT OUTER JOIN EMPLOYEES E
ON E.EMPLOYEEID = D.EMPLOYEEID

-- 6. Use a SELF JOIN to select the employee first name, last name, title as well as the manager's first name, last name and title.
SELECT E.FIRSTNAME EMPLOYEE_FIRSTNAME, E.LASTNAME EMPLOYEE_LASTNAME, E.TITLE EMPLOYEE_TITLE, M.FIRSTNAME MANAGER_FIRSTNAME, M.LASTNAME MANAGER_LASTNAME, M.TITLE MANAGER_TITLE
FROM EMPLOYEES E
INNER JOIN EMPLOYEES M 
ON M.EMPLOYEEID = E.MANAGERID
